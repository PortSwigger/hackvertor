function unescapeJsonString(input) {
  return input.replace(/\\["\\\/]|\\u[0-9A-Fa-f]{4}/g, match => {
    if (chars[match]) {
      return chars[match];
    } else if (match.startsWith("\\u")) {
      return String.fromCharCode(parseInt(match.slice(2), 16));
    }
    return match; // In case of any unexpected match
  });
}
